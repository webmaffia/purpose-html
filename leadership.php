<!-- Card pop-up functionality -->
<script>
    const cards = document.querySelectorAll(".leadership__employee-card");
    const modal = document.getElementById("leadershipModal");
    const modalImage = document.getElementById("modalImage");
    const modalText = document.getElementById("modalText");
    const modalName = document.getElementById("modalName");
    const modalRole = document.getElementById("modalRole");
    const modalClose = document.getElementById("modalClose");
    const modalLeft = document.querySelector(".leadership__modal-left");

    cards.forEach((card, index) => {
        card.addEventListener("click", () => {

            // Set modal content
            modalImage.src = card.dataset.image;
            modalText.textContent = card.dataset.quote;
            modalName.textContent = card.dataset.name;
            modalRole.textContent = card.dataset.role;

            // Remove previous background classes
            modalLeft.classList.remove("green-bg", "grey-bg");

            // Toggle based on card position 
            if ((index + 1) % 2 === 0) {
                modalLeft.classList.add("grey-bg");
            } else {
                modalLeft.classList.add("green-bg");
            }

            modal.classList.add("active");
        });
    });

    modalClose.addEventListener("click", () => {
        modal.classList.remove("active");
    });

    modal.addEventListener("click", (e) => {
        if (e.target === modal) {
            modal.classList.remove("active");
        }
    });
</script>

