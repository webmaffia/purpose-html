	<script>
	(function() {
		var svg = document.getElementById('chronicles-union-svg');
		var profiles = document.querySelectorAll('.section-chronicles-bg__profile-wrap');
		var unpauseTimer = null;
		if (!svg || !profiles.length) return;
		function pause() {
			clearTimeout(unpauseTimer);
			unpauseTimer = null;
			if (svg.pauseAnimations) svg.pauseAnimations();
		}
		function unpause() {
			if (svg.unpauseAnimations) svg.unpauseAnimations();
		}
		profiles.forEach(function(el) {
			el.addEventListener('mouseenter', pause);
			el.addEventListener('mouseleave', function() {
				unpauseTimer = setTimeout(unpause, 80);
			});
		});
	})();

	(function() {
		var popup = document.getElementById('chronicles-video-popup');
		var video = popup && popup.querySelector('.chronicles-video-popup__video');
		var overlay = popup && popup.querySelector('.chronicles-video-popup__overlay');
		var closeBtn = popup && popup.querySelector('.chronicles-video-popup__close');
		var profiles = document.querySelectorAll('.section-chronicles-bg__profile-wrap');
		var videoSrc = 'assets/videos/3-Our Purpose -Standalone -High Resolution.mp4';
		if (!popup || !video) return;
		function openPopup() {
			popup.classList.add('chronicles-video-popup--open');
			popup.setAttribute('aria-hidden', 'false');
			video.src = videoSrc;
			video.play();
		}
		function closePopup() {
			popup.classList.remove('chronicles-video-popup--open');
			popup.setAttribute('aria-hidden', 'true');
			video.pause();
		}
		profiles.forEach(function(el) {
			el.addEventListener('click', function(e) {
				e.preventDefault();
				openPopup();
			});
		});
		if (overlay) overlay.addEventListener('click', closePopup);
		if (closeBtn) closeBtn.addEventListener('click', closePopup);
	})();
	</script>
